import { useState } from "react";
import { useRouter } from "next/navigation";

export default function AdminLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const pathname = usePathname();
  const router = useRouter();
  const [menuOpen, setMenuOpen] = useState(false);

  const navItems = [
    { name: "Dashboard", href: "/admin", icon: Icons.Dashboard },
    { name: "Pesanan", href: "/admin/orders", icon: Icons.Orders },
    { name: "Katalog Template", href: "/admin/templates", icon: Icons.Templates },
    { name: "Pengaturan", href: "/admin/settings", icon: Icons.Settings },
  ];

  const handleLogout = async () => {
    try {
      await fetch("/api/admin/logout", { method: "POST" });
      router.push("/admin/login");
      router.refresh();
    } catch (error) {
      console.error("Logout failed", error);
    }
  };

  // Jika ini halaman login, jangan tampilkan sidebar dan header admin
  if (pathname === "/admin/login") {
    return <>{children}</>;
  }

  return (
    <div className="min-h-screen bg-slate-50 flex font-sans">
      {/* Admin Sidebar - Dark Theme for distinction */}
      <aside className="w-64 bg-slate-900 text-white flex flex-col sticky top-0 h-screen shadow-2xl">
        <div className="p-8 border-b border-slate-800">
          <Link href="/admin" className="flex items-center gap-2">
            <span className="text-xl font-bold tracking-tight">
              MyPromise <span className="text-rose-500 font-black">HQ</span>
            </span>
          </Link>
          <p className="text-[10px] text-slate-500 uppercase tracking-widest mt-1 font-bold">Operator Console</p>
        </div>

        <nav className="flex-grow p-6 space-y-1">
          {navItems.map((item) => {
            const isActive = pathname === item.href;
            const Icon = item.icon;
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`flex items-center gap-3 px-4 py-3 transition-all rounded-xl text-sm ${
                  isActive 
                    ? "bg-slate-800 text-white font-bold" 
                    : "text-slate-400 hover:bg-slate-800 hover:text-white font-medium"
                }`}
              >
                <Icon /> {item.name}
              </Link>
            );
          })}
        </nav>

        <div className="p-6 border-t border-slate-800 space-y-2">
          <button 
            onClick={handleLogout}
            className="w-full px-4 py-3 text-left text-rose-400 hover:text-white transition-colors text-xs font-bold uppercase tracking-widest flex items-center gap-3"
          >
            <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="3" strokeLinecap="round" strokeLinejoin="round">
              <path d="M9 21H5a2 2 0 0 1-2-2V5a2 2 0 0 1 2-2h4"></path>
              <polyline points="16 17 21 12 16 7"></polyline>
              <line x1="21" y1="12" x2="9" y2="12"></line>
            </svg>
            Logout HQ
          </button>
          <Link href="/" className="w-full px-4 py-3 text-left text-slate-500 hover:text-slate-300 transition-colors text-[9px] font-bold uppercase tracking-widest flex items-center gap-3">
            <Icons.ArrowLeft /> View Website
          </Link>
        </div>
      </aside>

      {/* Admin Content Area */}
      <div className="flex-grow flex flex-col">
        <main className="p-10 max-w-7xl w-full mx-auto">
          {children}
        </main>
      </div>
    </div>
  );
}

